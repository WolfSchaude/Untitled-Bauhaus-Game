using UnityEngine.UI;

public class EventSave
{
	public int[] Wait;
	public MemorySave[] Run;
	public int[] Fin;

	public EventSave(int[] wait, MemorySave[] run, int[] fin)
	{
		Wait = wait;
		Run = run;
		Fin = fin;
	}
}

public class EventScript : MonoBehaviour, ISaveableInterface
{
