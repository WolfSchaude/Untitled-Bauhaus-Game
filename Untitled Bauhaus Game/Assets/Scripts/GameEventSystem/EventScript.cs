	public List<GameObject> AllEvents;
	public GameObject ThatOneRandomEvent;

	public Text AnzahlEvents;

	private bool AlreadyGenerated = false;

	private int ActiveEvents;

	bool Laedtgerade;

	//-------------------------------( Addition for InWorldEvents )-------------------------------//
